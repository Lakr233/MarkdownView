        \\[ x + (x + 20) = 200 \\]
    *   **解答**：\\(2x + 20 = 200\\)  ➡️  \\(2x = 200 - 20\\)  ➡️  \\(2x = 180\\)  ➡️  \\(x = 90\\)。
    *   **答案**：梨树有 **90** 棵，苹果树有 \\(90 + 20 = \textbf{110}\\) 棵。

